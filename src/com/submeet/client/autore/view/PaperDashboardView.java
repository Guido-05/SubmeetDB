    private void tableReviewsMouseClicked(java.awt.event.MouseEvent evt) {
        // Get the selected row
        int row = tableReviews.getSelectedRow();
        if (row >= 0) {
            // Get the conference ID from the first column
            Integer paperId = (Integer) paperList.get(row).get("paperId");
            // Show conference info
            authorConferenceControl.showPaperInfoView(paperId, conferenceId, conferenceTitle, tableReviews.getValueAt(row, 1).toString());
            // Close this window
            this.dispose();
        }
    }

