    private void displayPaperList() {
        this.fieldConference.setText(conferenceTitle);
        this.fieldConference.setDisabledTextColor(new java.awt.Color(0, 0, 0));

        DefaultTableModel model = (DefaultTableModel) tableReviews.getModel();
        model.setRowCount(0); // Clear the table

        if (paperList != null && !paperList.isEmpty()) {
            for (Map<String, Object> paper : paperList) {

                String paperState;
                if((Integer) paper.get("accepted") == 1)
                {
                    paperState = "Accettato";
                }
                else if((Integer) paper.get("accepted") == -1)
                {
                    paperState = "Rifiutato";
                }
                else
                {
                    if((Boolean) paper.get("isFinalVersion"))
                    {
                        paperState = "Versione Finale";
                    }
                    else
                    {
                        List<EntityReview> reviews = DBMSBoundary.getPaperInfo((int) paper.get("paperId"));

                        // Check if the paper has reviews
                        if(!reviews.isEmpty())
                        {
                            paperState = "Revisionato";
                            // Check if there are reviews not done
                            for (EntityReview review : reviews)
                            {
                                if (!review.getState().equals("done"))
                                {
                                    paperState = "In Revisione";
                                    break;
                                }
                            }
                        }
                        else
                        {
                            // If the paper has no reviews
                            paperState = "Non assegnato";
                        }
                    }
                }

                model.addRow(new Object[]{
                        paper.get("title"),
                        paperState
                });
            }
        }

    }

