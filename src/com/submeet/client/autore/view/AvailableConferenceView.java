    private void tableConferencesMouseClicked(java.awt.event.MouseEvent evt) {
        // Get the selected row
        int row = tableConferences.getSelectedRow();
        if (row >= 0) {
            // Get the conference ID from the first column
            int conferenceId = (int) tableConferences.getValueAt(row, 0);
            // Show conference info
            authorConferenceControl.showConferenceInfo(conferenceId);
            // Close this window
            this.dispose();
        }
    }

