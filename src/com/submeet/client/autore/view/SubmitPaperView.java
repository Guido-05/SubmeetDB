    private void uploadPaperButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // Create a file chooser
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Seleziona il file del paper");

        // Set file filter to only show PDF files
        fileChooser.setFileFilter(new javax.swing.filechooser.FileFilter() {
            @Override
            public boolean accept(File f) {
                return f.isDirectory() || f.getName().toLowerCase().endsWith(".pdf");
            }

            @Override
            public String getDescription() {
                return "PDF Files (*.pdf)";
            }
        });

        // Show the dialog and get the result
        int result = fileChooser.showOpenDialog(this);

        // If a file was selected, store it
        if (result == JFileChooser.APPROVE_OPTION) {
            selectedFile = fileChooser.getSelectedFile();
            uploadPaperButton.setText("FILE SELEZIONATO: " + selectedFile.getName());
        }
    }

