    private void submitPaperButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // Get paper title and description
        String title = fieldPaperTitle.getText();
        String description = textAreaDescription.getText();

        // Validate input
        if (title == null || title.isBlank()) {
            new ErrorPopupView("Il titolo del paper non può essere vuoto.");
            return;
        }

        if (description == null || description.isBlank()) {
            new ErrorPopupView("La descrizione del paper non può essere vuota.");
            return;
        }

        if (selectedFile == null) {
            new ErrorPopupView("Devi allegare un file per il paper.");
            return;
        }

        // Get selected specializations
        StringBuilder specializationsBuilder = new StringBuilder();
        Component[] components = panelInterestAreas.getComponents();
        for (Component component : components) {
            if (component instanceof JCheckBox) {
                JCheckBox checkBox = (JCheckBox) component;
                if (checkBox.isSelected()) {
                    if (specializationsBuilder.length() > 0) {
                        specializationsBuilder.append(",");
                    }
                    specializationsBuilder.append(checkBox.getActionCommand()); // This contains the specializationId
                }
            }
        }
        String specializations = specializationsBuilder.toString();

        if (specializations.isEmpty()) {
            new ErrorPopupView("Devi selezionare almeno un'area di interesse.");
            return;
        }

        // Submit the paper
        if (authorConferenceControl.submitPaper(AppSession.getInstance().getUserId(), conference.getConferenceId(),
            title, description, specializations, selectedFile)) {
            this.dispose();
        }

    }

