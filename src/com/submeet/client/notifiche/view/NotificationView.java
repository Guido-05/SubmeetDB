    private void homeButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // Create HomeControl and show HomeRouterView
        HomeControl homeControl = new HomeControl();
        homeControl.createHomeRouterView();
        // Dispose this view
        this.dispose();
    }

    private void backButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // Create NotificationCenterControl and show NotificationCenterView
        // TODO: sistemare qusto back button
        NotificationCenterControl control = new NotificationCenterControl(AppSession.getInstance().getUserId());
        List<EntityNotification> notifications = control.getUserNotifications(notification.getUserId());
        NotificationCenterView view = new NotificationCenterView(notifications, control);
        view.setVisible(true);
        // Dispose this view
        this.dispose();
    }

    // Button accept invite
    private void buttonAcceptActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO: mettere logica nella control, vedi sequence
        if (notification != null) {
            // Accept the invite
            if(DBMSBoundary.acceptInvite(notification.getNotificId())) {
                new SuccessPopupView("Invito accettato con successo");
                backButtonActionPerformed(evt);
            } else {
                new ErrorPopupView("Errore durante l'accettazione dell'invito");
            }
        }
    }

    // Button deny invite
    private void buttonDenyActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO: mettere logica nella control, vedi sequence
        if (notification != null) {
            // Deny the invite
            if(DBMSBoundary.denyInvite(notification.getNotificId())) {
                new SuccessPopupView("Invito rifiutato con successo");
                backButtonActionPerformed(evt);
            } else {
                new ErrorPopupView("Errore durante il rifiuto dell'invito");
            }
        }
    }

