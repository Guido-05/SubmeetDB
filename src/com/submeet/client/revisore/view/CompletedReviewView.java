    /**
     * Populates the UI components with data from the review
     */
    private void populateFields() {
        if (review != null) {
            // Set paper title
            fieldPaperTitle.setText(review.getTitle());
            fieldPaperTitle.setDisabledTextColor(new java.awt.Color(0, 0, 0));

            // Set review text
            textAreaReview.setText(review.getReviewComment());

            // Set private comment
            textAreaPrivateComment.setText(review.getPrivateComment());

            // Set rating stars
            int rating = review.getRating();

            // Disable all toggle buttons
            toggleButtonStar1.setEnabled(false);
            toggleButtonStar2.setEnabled(false);
            toggleButtonStar3.setEnabled(false);
            toggleButtonStar4.setEnabled(false);
            toggleButtonStar5.setEnabled(false);

            // Set selected state based on rating
            toggleButtonStar1.setSelected(rating >= 1);
            toggleButtonStar2.setSelected(rating >= 2);
            toggleButtonStar3.setSelected(rating >= 3);
            toggleButtonStar4.setSelected(rating >= 4);
            toggleButtonStar5.setSelected(rating >= 5);
        }
    }

