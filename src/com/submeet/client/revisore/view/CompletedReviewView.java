    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
        // Return to home screen using the ReviewerConferenceControl
        if (reviewerConferenceControl != null) {
            // Close this view
            this.dispose();
            // Return to home
            reviewerConferenceControl.returnToHome();
        }
    }

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {
        // Go back to the previous screen
        if (reviewerConferenceControl != null) {
            // Close this view
            this.dispose();
            // Show the reviewer dashboard view
            if(!isSubReview)
                reviewerConferenceControl.showReviewerDashboardView();
            else
                reviewerConferenceControl.returnToReviewerView();
        }
    }

    private void othersReviewButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // Show other reviews for this paper
        if (reviewerConferenceControl != null && review != null) {
            // Show the other reviews view
            if(!isSubReview) {
                this.dispose();
                reviewerConferenceControl.showOtherReviewView(review.getPaperId(), review.getTitle());
            }
            else
                new ErrorPopupView("Non puoi vedere revisioni altrui se sei un sotto revisore");
        }
    }

    private void downloadPaperButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // Download the paper using the ReviewerConferenceControl
        if (reviewerConferenceControl != null && review != null) {
            boolean success = reviewerConferenceControl.downloadPaper(review.getPaperId(), review.getTitle());
            if (!success) {
                new ErrorPopupView("Download del file fallito");
            }
        }
    }

