    private void preferencesButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // Extract papers with preference and/or conflict
        List<Integer> paperIds = new ArrayList<>();
        List<Integer> preferences = new ArrayList<>();
        List<Boolean> conflicts = new ArrayList<>();

        DefaultTableModel model = (DefaultTableModel) tablePapers.getModel();

        for (int i = 0; i < model.getRowCount(); i++) {
            // Get paper ID from unassignedPapers (assumed same order as table rows)
            int paperId = unassignedPapers.get(i).getPaperId();

            // Get preference from JComboBox
            String preferenceStr = (String) model.getValueAt(i, 2);
            int preference = Integer.parseInt(preferenceStr);

            // Get conflict from JCheckBox
            Boolean conflict = (Boolean) tablePapers.getValueAt(i, 3);

            // Skip if preference is 0 and no conflict
            if (preference == 0 && !conflict) {
                continue;
            }

            paperIds.add(paperId);
            preferences.add(preference);
            conflicts.add(conflict);
        }

        reviewerConferenceControl.confirmInterest(paperIds, preferences, conflicts);
        this.dispose();
    }

