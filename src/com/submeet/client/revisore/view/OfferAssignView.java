    public void dipsplayPapers() {
        this.fieldConference.setText(conferenceTitle);
        this.fieldConference.setEditable(false);
        this.fieldConference.setDisabledTextColor(new java.awt.Color(0, 0, 0));

        DefaultTableModel model = new DefaultTableModel(new Object[]{"Autore", "Titolo", "Preferenza", "Conflitto"}, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return column == 2 || column == 3; // Solo Preference e Conflict editabili
            }

            @Override
            public Class<?> getColumnClass(int columnIndex) {
                return switch (columnIndex) {
                    case 3 -> Boolean.class; // JCheckBox
                    default -> String.class;
                };
            }
        };
        model.setRowCount(0); // Clear the table

        model.addTableModelListener(e -> {
            int row = e.getFirstRow();
            int column = e.getColumn();

            // Solo se è la colonna di preferenza o conflitto
            if (column == 2 || column == 3) {
                String prefValue = (String) model.getValueAt(row, 2);
                Boolean conflict = (Boolean) model.getValueAt(row, 3);

                // Se conflitto è true, forza preferenza a "0"
                if (column == 3 && conflict != null && conflict) {
                    if (!"0".equals(prefValue)) {
                        model.setValueAt("0", row, 2);
                    }
                }

                // Se preferenza > 0, forza conflitto a false
                if (column == 2 && prefValue != null && !"0".equals(prefValue)) {
                    if (conflict != null && conflict) {
                        model.setValueAt(false, row, 3);
                    }
                }
            }
        });

        tablePapers.setModel(model);

        // Preference column (index 2): ComboBox
        JComboBox<String> preferenceBox = new JComboBox<>(new String[]{"0", "1", "2", "3", "4", "5"});
        tablePapers.getColumnModel().getColumn(2).setCellEditor(new DefaultCellEditor(preferenceBox));

        // Conflict column (index 3): CheckBox
        tablePapers.getColumnModel().getColumn(3).setCellEditor(new DefaultCellEditor(new JCheckBox()));

        if (unassignedPapers != null && !unassignedPapers.isEmpty()) {
            for (EntityPaper paper : unassignedPapers) {

                model.addRow(new Object[]{
                        paper.getAuthorName() + " " + paper.getAuthorSurname(),
                        paper.getTitle(),
                        "0",
                        false
                });
            }
        }
    }

